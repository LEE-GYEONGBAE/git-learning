public class Barista {
    CoffeeMachine machine = new CoffeeMachine();

    public void turnOn() {
        machine.turnOn();
    }

    public void fillCoffee() {
        machine.fillCoffee();
    }

    public void fillWater() {
        machine.fillWater();
    }

    public void makeCoffee() {
        machine.makeCoffee();
    }

    public void clearMachine() {
        machine.clean();
    }

    public void turnOff() {
        machine.turnOff();
    }

    public void info() {
        machine.info();
    }
}
